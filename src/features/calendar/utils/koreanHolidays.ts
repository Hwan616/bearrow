function zeroPad(n: number): string {
  return n.toString().padStart(2, "0");
}

/**
 * 날짜에 해당하는 공휴일 이름을 반환한다. 공휴일이 아니면 null.
 * 양력 고정 공휴일과 음력 기반 공휴일(사전 계산) 모두 확인한다.
 */
export function getHolidayName(date: Date): string | null {
  const mm = zeroPad(date.getMonth() + 1);
  const dd = zeroPad(date.getDate());
  const yyyy = date.getFullYear().toString();

  // 음력 기반이 양력 고정보다 우선 (겹치는 날짜는 더 구체적인 이름 사용)
  return (
    LUNAR_HOLIDAYS[`${yyyy}-${mm}-${dd}`] ??
    SOLAR_FIXED[`${mm}-${dd}`] ??
    null
  );
}

/**
 * 특정 연월의 공휴일 맵을 반환한다.
 * key: 일(day, 1-31), value: 공휴일 이름
 */
export function getHolidaysForMonth(year: number, month: number): Map<number, string> {
  const result = new Map<number, string>();
  const daysInMonth = new Date(year, month + 1, 0).getDate();

  for (let day = 1; day <= daysInMonth; day++) {
    const name = getHolidayName(new Date(year, month, day));
    if (name) result.set(day, name);
  }

  return result;
}
